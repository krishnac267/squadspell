'use strict';

/**
 * Builds dictionary-meanings.js: part-of-speech + at least one definition per VALID_WORDS entry.
 * Uses api.dictionaryapi.dev; words with no API entry get a short list fallback.
 * Resume: re-run — merges with existing dictionary-meanings.js if present.
 */

const fs = require('fs');
const path = require('path');
const https = require('https');

const root = path.join(__dirname, '..');
const dictPath = path.join(root, 'dictionary.js');
const outPath = path.join(root, 'dictionary-meanings.js');

const MAX_SECTIONS = 2;
const MAX_DEFS_PER_SECTION = 1;
const CONCURRENCY = 4;
const MS_BETWEEN_BATCH = 120;

const LIST_FALLBACK = {
  p: 'phrase',
  d: "In this game's bundled tournament-style word list; no separate dictionary entry on the free API."
};

function loadWordList() {
  const t = fs.readFileSync(dictPath, 'utf8');
  const i = t.indexOf('new Set(');
  const s = t.indexOf('`', i) + 1;
  const e = t.indexOf('`.trim()', s);
  return t
    .slice(s, e)
    .trim()
    .split(/\s+/)
    .filter(Boolean)
    .map(w => w.toUpperCase());
}

function loadExisting() {
  if (!fs.existsSync(outPath)) return {};
  const raw = fs.readFileSync(outPath, 'utf8');
  const m = raw.match(/BUNDLED_WORD_MEANING_ROWS\s*=\s*(\{[\s\S]*\});/);
  if (!m) return {};
  try {
    return JSON.parse(m[1]);
  } catch {
    return {};
  }
}

function fetchApi(word) {
  const url = `https://api.dictionaryapi.dev/api/v2/entries/en/${encodeURIComponent(word.toLowerCase())}`;
  return new Promise((resolve, reject) => {
    https
      .get(url, { headers: { 'User-Agent': 'SquadSpell-dictionary-build/1.0' } }, res => {
        let body = '';
        res.on('data', c => (body += c));
        res.on('end', () => {
          if (res.statusCode !== 200) {
            resolve({ _status: res.statusCode, _raw: body });
            return;
          }
          try {
            resolve(JSON.parse(body));
          } catch (e) {
            reject(e);
          }
        });
      })
      .on('error', reject)
      .setTimeout(25000, function onTimeout() {
        this.destroy();
        reject(new Error('timeout'));
      });
  });
}

function compactFromApi(data) {
  if (!data || !Array.isArray(data) || data.length === 0) return null;
  const entry = data[0];
  const meaningsRaw = entry.meanings;
  if (!meaningsRaw || !Array.isArray(meaningsRaw)) return null;
  const rows = [];
  let sections = 0;
  for (const m of meaningsRaw) {
    if (sections >= MAX_SECTIONS) break;
    if (!m || typeof m !== 'object') continue;
    const partOfSpeech = m.partOfSpeech ? String(m.partOfSpeech).trim() : '';
    const defs = m.definitions;
    if (!defs || !Array.isArray(defs)) continue;
    let added = 0;
    for (const d of defs) {
      if (added >= MAX_DEFS_PER_SECTION) break;
      if (!d || typeof d !== 'object') continue;
      const defStr = d.definition ? String(d.definition).trim() : '';
      if (!defStr) continue;
      rows.push({ p: partOfSpeech || 'unknown', d: defStr });
      added++;
    }
    if (added) sections++;
  }
  return rows.length ? rows : null;
}

async function main() {
  const words = loadWordList();
  const map = loadExisting();
  const missing = words.filter(w => !map[w]);
  console.log('Total words:', words.length, '| Already built:', words.length - missing.length, '| To fetch:', missing.length);

  let idx = 0;
  async function worker() {
    while (idx < missing.length) {
      const my = idx++;
      const w = missing[my];
      try {
        const data = await fetchApi(w);
        if (data && data._status) {
          map[w] = [LIST_FALLBACK];
        } else {
          const rows = compactFromApi(data);
          map[w] = rows && rows.length ? rows : [LIST_FALLBACK];
        }
      } catch {
        map[w] = [LIST_FALLBACK];
      }
      if ((my + 1) % 200 === 0) {
        console.log('Progress', my + 1, '/', missing.length);
        flush(map);
      }
    }
  }

  function flush(m) {
    const json = JSON.stringify(m);
    const body = `'use strict';\n/* Auto-generated by scripts/build-dictionary-meanings.cjs — do not edit by hand. */\nvar BUNDLED_WORD_MEANING_ROWS = ${json};\nif (typeof module !== 'undefined' && module.exports) {\n  module.exports = { BUNDLED_WORD_MEANING_ROWS };\n}\n`;
    fs.writeFileSync(outPath, body, 'utf8');
  }

  const workers = [];
  for (let i = 0; i < CONCURRENCY; i++) {
    workers.push(
      (async () => {
        while (idx < missing.length) {
          await workerOne();
          await new Promise(r => setTimeout(r, MS_BETWEEN_BATCH));
        }
      })()
    );
  }

  let wIndex = 0;
  async function workerOne() {
    const my = wIndex++;
    if (my >= missing.length) return;
    const w = missing[my];
    try {
      const data = await fetchApi(w);
      if (data && data._status) {
        map[w] = [LIST_FALLBACK];
      } else {
        const rows = compactFromApi(data);
        map[w] = rows && rows.length ? rows : [LIST_FALLBACK];
      }
    } catch {
      map[w] = [LIST_FALLBACK];
    }
    if ((my + 1) % 200 === 0) {
      console.log('Progress', my + 1, '/', missing.length);
      flush(map);
    }
  }

  // Fix race: simplify to sequential pool
  idx = 0;
  wIndex = 0;
  async function runPool() {
    async function runOne() {
      for (;;) {
        const i = wIndex++;
        if (i >= missing.length) break;
        const w = missing[i];
        try {
          const data = await fetchApi(w);
          if (data && data._status) {
            map[w] = [LIST_FALLBACK];
          } else {
            const rows = compactFromApi(data);
            map[w] = rows && rows.length ? rows : [LIST_FALLBACK];
          }
        } catch {
          map[w] = [LIST_FALLBACK];
        }
        if ((i + 1) % 200 === 0) {
          console.log('Progress', i + 1, '/', missing.length);
          flush(map);
        }
        await new Promise(r => setTimeout(r, MS_BETWEEN_BATCH));
      }
    }
    await Promise.all(Array.from({ length: CONCURRENCY }, () => runOne()));
  }

  await runPool();
  flush(map);
  console.log('Wrote', outPath, 'keys:', Object.keys(map).length);
}

main().catch(e => {
  console.error(e);
  process.exit(1);
});
